import java.util.Scanner;

public class StaniBogat2 {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);

        String[] questions = {
                "Коя е столицата на България?",
                "Кой философ въвежда понятието 'воля за власт'?",
                "Кой е написал романа 'Под игото'?",
                "Кой е химичният символ на водата?",
                "Кой философ заявява: 'Аз мисля, следователно съществувам'?",
                "Какво е столицата на Австралия?",
                "Кой е открил закона за гравитацията?",
                "Кое е най-голямото животно на Земята?",
                "Кой е автор на романа '1984'?",
                "Коя планета е най-близо до Слънцето?",
                "Кой композитор е написал 'Лунна соната'?",
                "Коя е най-дългата река в света?",
                "Кой е създателят на теорията на относителността?",
                "Кое е химичното означение за злато?",
                "Коя държава е домакин на Олимпийските игри през 2008 г.?"
        };

        String[][] options = {
                {"A) Пловдив", "B) Варна", "C) София", "D) Бургас"},
                {"A) Хегел", "B) Ницше", "C) Шопенхауер", "D) Сартр"},
                {"A) Елин Пелин", "B) Иван Вазов", "C) Христо Ботев", "D) Пенчо Славейков"},
                {"A) O2", "B) CO2", "C) H2O", "D) HO2"},
                {"A) Аристотел", "B) Декарт", "C) Платон", "D) Кант"},
                {"A) Мелбърн", "B) Канбера", "C) Сидни", "D) Пърт"},
                {"A) Айнщайн", "B) Нютон", "C) Галилей", "D) Планк"},
                {"A) Африкански слон", "B) Син кит", "C) Жираф", "D) Бял носорог"},
                {"A) Джордж Оруел", "B) Олдъс Хъксли", "C) Рей Бредбъри", "D) Марк Твен"},
                {"A) Венера", "B) Земя", "C) Меркурий", "D) Марс"},
                {"A) Моцарт", "B) Шопен", "C) Бетовен", "D) Бах"},
                {"A) Нил", "B) Амазонка", "C) Янцзъ", "D) Мисисипи"},
                {"A) Нютон", "B) Айнщайн", "C) Тесла", "D) Дарвин"},
                {"A) Ag", "B) Au", "C) Zn", "D) Pt"},
                {"A) Гърция", "B) Великобритания", "C) Китай", "D) САЩ"}
        };

        char[] correctAnswers = {
                'C','B','B','C','B','B','B','B',
                'A','C','C','B','B','B','C'
        };

        int[] levels = {100, 150, 190, 200, 300, 500, 1000, 1050,
                1200, 1500, 2000, 2500, 5000, 7500, 10000};

        int prize = 0;

        System.out.println("Добре дошъл в играта „СТАНИ БОГАТ“!");
        System.out.println("Отговаряй с A, B, C или D.\n");

        for (int i = 0; i < questions.length; i++) {

            System.out.println("Въпрос " + (i + 1) + ": " + questions[i]);
            for (String opt : options[i]) {
                System.out.println(opt);
            }

            // Проверка дали отговорът е A/B/C/D
            char answer;
            while (true) {
                System.out.print("Твоят отговор: ");
                answer = Character.toUpperCase(input.next().charAt(0));

                if (answer == 'A' || answer == 'B' || answer == 'C' || answer == 'D') {
                    break;
                } else {
                    System.out.println("Няма такъв отговор! Избери от A, B, C или D.\n");
                }
            }

            if (answer == correctAnswers[i]) {
                prize = levels[i];
                System.out.println("Верен отговор! Печелиш " + prize + " лева.\n");

                if (i < questions.length - 1) {
                    System.out.print("Искаш ли да продължиш? (y/n): ");
                    String decision = input.next().toLowerCase();

                    if (!decision.equals("y")) {
                        System.out.println("Ти напускаш играта с " + prize + " лева!");
                        break;
                    }
                } else {
                    System.out.println("Поздравления! Победи играта и спечели " + prize + " лева!");
                }

            } else {
                System.out.println("Грешен отговор! Тръгваш си с 0 лева.");
                break;
            }
        }

        input.close();
    }
}